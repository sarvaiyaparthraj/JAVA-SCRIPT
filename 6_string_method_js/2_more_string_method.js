
let text = "Sarvaiya Parthrajsinh";

// match
console.log("match string :", text.match("Parth"));

// no match
console.log("not found :", text.match("xyz"));

// repeat
console.log("repeat text :", text.repeat(2));

// replace
console.log(
  "replace word :",
  text.replace("Parthrajsinh", "Rajsinh")
);

// replaceAll
console.log(
  "replace all :",
  text.replaceAll("a", "@")
);

// search
console.log("search result :", text.search("r"));

// indexOf
console.log("index value :", text.indexOf("S"));

// slice
console.log("original text :", text);

console.log("slice 1 :", text.slice(1));

console.log("slice 2 :", text.slice(2, 8));

console.log("slice 3 :", text.slice(5, 12));

console.log("slice negative :", text.slice(-4));





